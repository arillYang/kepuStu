package com.kepu.pojo.user.active;

public class BaseSS {
	private int view;
	private int people;
	private int total;
	private String name;
	private double active;
	public int getView() {
		return view;
	}
	public void setView(int view) {
		this.view = view;
	}
	public int getPeople() {
		return people;
	}
	public void setPeople(int people) {
		this.people = people;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public double getActive() {
		return active;
	}
	public void setActive(double active) {
		this.active = active;
	}
	
}
